    public void CrearTabla() {
        String titulos[] = {"Codigo", "Categoria"};
        modelo.setColumnIdentifiers(titulos);
        TablaCategorias.setModel(modelo);
    }

    public void CargarTabla() {
        ArrayList<Categoria> lc = new ArrayList();
        LimpiarTabla();
        lc = cc.ListadoCategorias();

        for (Categoria c : lc) {
            String datos[] = {c.getCodigo(), c.getCategoria()};
            modelo.addRow(datos);
        }

    }

    public void LimpiarForm() {
        this.txtCategoria.setText("");
        this.txtCodigo.setText("");
    }

    public void LimpiarTabla() {
        int filas = TablaCategorias.getRowCount();
        for (int i = 0; filas > i; i++) {
            modelo.removeRow(0);
        }
    }

    public void CargarForm(Categoria c) {
        txtCategoria.setText(c.getCategoria());
        txtCodigo.setText(c.getCodigo());
    }

