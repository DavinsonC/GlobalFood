    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if (txtCodigo.getText().isEmpty() || txtCategoria.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe Ingresar todos los campos de informacion");
        } else {
            Categoria c = new Categoria();
            c.setCategoria(txtCategoria.getText());
            c.setCodigo(txtCodigo.getText());

            int ex = cc.RegistrarCategoria(c);

            if (ex == 0) {
                JOptionPane.showMessageDialog(null, "Error al registrar Categoria");
            } else {
                LimpiarForm();
                CargarTabla();
                JOptionPane.showMessageDialog(null, "Categoria Registrada exitosamente !!!!");
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        if (txtCodigo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el codigo de una categoria");
        } else {
            Categoria c = null;
            c = cc.RetornarCategoriaxCod(txtCodigo.getText());
            if (c == null) {
                JOptionPane.showMessageDialog(null, "No se encontro la categoria  a buscar");
            } else {
                CargarForm(c);
                JOptionPane.showMessageDialog(null, "Categoria Encontrada");
            }
        }


    }//GEN-LAST:event_jButton2ActionPerformed

