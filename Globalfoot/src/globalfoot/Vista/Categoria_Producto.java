    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        Categoria c = new Categoria();
        if (txtCodigo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el codigo de una categoria");
        } else {
            c.setCategoria(txtCategoria.getText());
            c.setCodigo(txtCodigo.getText());
            if (cc.ModificarCategoria(c) == 1) {
                LimpiarForm();
                CargarTabla();
                JOptionPane.showMessageDialog(null, "Se modifico correctamente la categoria !!!!");
            } else {
                JOptionPane.showMessageDialog(null, "Error al modificar Categoria !!!!");
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

